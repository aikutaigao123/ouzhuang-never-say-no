        • 应用完全关闭后
        • 任何其他非"寻找"功能的使用场景
        
        **匹配信息时效性说明**：
        
        • 您匹配到的所有用户信息都是该用户最后点击"寻找"按钮瞬间上传的信息
        • 这些信息包括位置记录、用户信息、头像等，反映了用户点击"寻找"按钮时的状态
        • 匹配信息不是实时的，而是基于用户最后主动上传的数据
