import SwiftUI

struct PrivacyPolicyView: View {
    @Environment(\.dismiss) private var dismiss
    @State private var privacyContent: String = ""
    @State private var isLoading: Bool = true
    @State private var errorMessage: String? = nil
    
    var body: some View {
        NavigationView {
            Group {
                if isLoading {
                    VStack {
                        ProgressView("正在加载隐私政策...")
                            .padding()
                    }
                } else if let error = errorMessage {
                    VStack {
                        Image(systemName: "exclamationmark.triangle")
                            .font(.largeTitle)
                            .foregroundColor(.orange)
                            .padding()
                        
                        Text("加载失败")
                            .font(.title2)
                            .fontWeight(.bold)
                            .padding(.bottom, 5)
                        
                        Text(error)
                            .font(.body)
                            .foregroundColor(.secondary)
                            .multilineTextAlignment(.center)
                            .padding(.horizontal)
                        
                        Button("重试") {
                            loadPrivacyPolicy()
                        }
                        .padding()
                        .background(Color.blue)
                        .foregroundColor(.white)
                        .cornerRadius(8)
                        .padding(.top)
                    }
                } else {
                    ScrollView {
                        VStack(alignment: .leading, spacing: 16) {
                            Text(privacyContent)
                                .font(.body)
                                .lineLimit(nil)
                                .multilineTextAlignment(.leading)
                        }
                        .padding()
                    }
                }
            }
            .navigationTitle("隐私政策")
            .navigationBarTitleDisplayMode(.inline)
            .toolbar {
                ToolbarItem(placement: .navigationBarTrailing) {
                    Button("关闭") {
                        dismiss()
                    }
                }
            }
        }
        .onAppear {
            loadPrivacyPolicy()
        }
    }
    
    private func loadPrivacyPolicy() {
        isLoading = true
        errorMessage = nil
        
        LeanCloudService.shared.fetchPrivacyPolicy { content, error in
            DispatchQueue.main.async {
                isLoading = false
                if error != nil {
                    // 如果从服务器获取失败，使用默认内容
                    privacyContent = getDefaultPrivacyPolicy()
                } else if let content = content, !content.isEmpty {
                    privacyContent = content
                } else {
                    // 如果服务器返回空内容，使用默认内容
                    privacyContent = getDefaultPrivacyPolicy()
                }
            }
        }
    }
    
